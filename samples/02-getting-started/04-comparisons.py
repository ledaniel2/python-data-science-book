x = 5
y = 2

# Equal to
result = x == y  # result = False

# Not equal to
result = x != y  # result = True

# Greater than
result = x > y   # result = True

# Less than
result = x < y   # result = False

# Greater than or equal to
result = x >= y  # result = True

# Less than or equal to
result = x <= y  # result = False
