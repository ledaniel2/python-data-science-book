x = 5
y = 2

# Addition
result = x + y   # result = 7

# Subtraction
result = x - y   # result = 3

# Multiplication
result = x * y   # result = 10

# Division
result = x / y   # result = 2.5

# Floor division
result = x // y  # result = 2

# Modulus
result = x % y   # result = 1

# Exponentiation
result = x ** y  # result = 25

# Negation
result = -x      # result = -5
